public class VD1{
    //主入口
    //变量学习
    public static void main(String[] args) {
        // 数据类型 变量名 = 变量值
        int a = 10;
        int b = 20;
        a = a + b; 
        System.out.println(a);

        //在一条语句中可以定义多个变量
        int c =12 , d = 13, e = 14;
        System.out.println(c);
        System.out.println(d);
        System.out.println(e);
    }
}