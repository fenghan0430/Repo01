public class VD3 {
    public static void main(String[] args) {

        //整数
        //byte -128~127
        byte a = 10;
        //short -32768~32767
        short b = 10;
        //int -21亿~21亿
        int c = 10;
        //long 很大很大
        //long 类型的变量最后要加L
        long d = 10;


        //浮点
        //float
        //定义float类型变量要加F为后缀
        float e = 10.1F;

        //double
        double f = 1.1;

        //字符
        //char 单个字符
        char g = 'a';
        
        //string 字符串，多字符
        String g2 = "世界";

        //布尔
        //boolean
        boolean h = true;
    }   
}
